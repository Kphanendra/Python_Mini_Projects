
number = 1234567

# 1.Convert number to binary, octal, hex
print("Binary value        : {:b}".format(number))
print("Octal value         : {:o}".format(number))
print("Hex value           : {:x}".format(number))

# 2.Format large numbers with commas
print("Number with commas  : {:,}".format(number))

# 3.Print scientific notation
print("Scientific value    : {:e}".format(number))